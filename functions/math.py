def add_integers(a: int, b: int) -> int:
    """
    Adds two integers and returns the result.
    Args:
        a (int): The first integer to be added.
        b (int): The second integer to be added.
    Returns:
        int: The sum of 'a' and 'b'.

    Examples:
        >>> add_integers(1, 2)
        3
        >>> add_integers(10, 20)
        30
        >>> add_integers(-5, 5)
        0
    """
    return a + b
